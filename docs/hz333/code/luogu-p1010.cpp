#include <iostream>
#include <string>
using namespace std;

/*
    函数名：solve
    作用  ：将一个非负整数 n 转换成题目要求的 “0,2 表示” 字符串
    参数  ：n —— 当前需要被表示的整数
    返回值：string —— n 的 0,2 表示形式（不含空格）
*/
string solve(int n) {

    // =====【递归出口 1】=====
    // n == 0 时，按照题意，直接返回 "0"
    if (n == 0) return "0";

    // =====【递归出口 2】=====
    // n == 1 表示 2^0，题目规定写成 2(0)
    if (n == 1) return "2(0)";

    /*
        res：用于拼接最终结果字符串
        比如：
        n = 137
        res 最终会变成：
        "2(2(2)+2+2(0))+2(2+2(0))+2(0)"
    */
    string res = "";

    /*
        从大到小枚举 2 的幂次
        因为 n <= 20000，2^14 = 16384 已经足够
        所以 i 从 14 遍历到 0
    */
    for (int i = 14; i >= 0; i--) {

        /*
            判断 n 的二进制中是否包含 2^i
            (1 << i) 表示 2 的 i 次方
        */
        if (n & (1 << i)) {

            // 如果 res 里已经有内容，说明前面已经拼过一项
            // 这时需要加一个 '+'
            if (!res.empty()) {
                res += "+";
            }

            /*
                根据指数 i 的不同，分类处理：
                --------------------------------
                i == 0 ：2(0)
                i == 1 ：2
                i >= 2 ：2( solve(i) )
            */

            if (i == 0) {
                // 2^0
                res += "2(0)";
            }
            else if (i == 1) {
                // 2^1
                res += "2";
            }
            else {
                // 2^i，其中 i 本身也需要递归表示
                res += "2(" + solve(i) + ")";
            }
        }
    }

    // 返回最终拼好的字符串
    return res;
}

int main() {

    int n;
    cin >> n;          // 输入正整数 n
    cout << solve(n);  // 输出 n 的 0,2 表示

    return 0;
}
